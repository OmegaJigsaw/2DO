// Para nombrar variables se usa la palabra funcion let de la siguiente manera
let nombre = "Texto en variables"

// Para llamar el nombre dentro de la funcion es de esta manera
console.log(nombre) // Esto funciona como un print de python

// Las variables no pueden comenzar con numeros en JS
// Las variables se pueden agrupar dependiendo de su capitalizacion
// En UpperCamelCase = Fuerza a que cada palabra dentro empieze en mayuscula
// En camelCase = Las palabras empiezan en mayusculas menos la primera
// En snake_case = Cada palabra debe ser separada con un guion bajo("_")

// se puede definir mas de una variable en una sola linea asi let variable1, variable2;
// y puede definir su valor inmediatamente o definirle el valor luego de declararla
